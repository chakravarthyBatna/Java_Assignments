CREATE TABLE EMPLOYEE (
    EMP_ID INT AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(100),
    GENDER VARCHAR(10),
    AGE INT,
    EMAIL VARCHAR(100)
);
CREATE TABLE ADDRESS (
    ADDRESS_ID INT AUTO_INCREMENT PRIMARY KEY,
    STATE VARCHAR(100),
    COUNTRY VARCHAR(100),
    PINCODE INT,
    EMP_ID INT,
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID)
);
